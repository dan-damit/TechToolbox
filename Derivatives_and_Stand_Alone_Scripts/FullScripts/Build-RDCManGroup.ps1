$servers = @(
    "UTILITY-1.vadtek.com",
    "AADCONNECT-1.vadtek.com",
    "DC-1.vadtek.com",
    "DC-2.vadtek.com",
    "DOCSTAR-1.vadtek.com",
    "E10-APP-1.vadtek.com",
    "E10-QUICKSHIP-1.vadtek.com",
    "E10-REPORT-1.vadtek.com",
    "E10-SQL-1.vadtek.com",
    "E10-TASK-1.vadtek.com",
    "EDI-2.vadtek.com",
    "FCTEMS-1.vadtek.com",
    "IDC-1.vadtek.com",
    "PRINTSRV-1.vadtek.com",
    "UTILITY-1.vadtek.com",
    "UTILITY-5.vadtek.com"
)

$rdgPath   = Join-Path $env:USERPROFILE "Documents\CoreServers.rdg"
$rootName  = "CoreServers"
$groupName = "Core Servers"

# Build minimal RDCMan skeleton (schemaVersion 3)
[xml]$xml = New-Object System.Xml.XmlDocument
$xml.LoadXml(@'
<?xml version="1.0" encoding="utf-8"?>
<RDCMan programVersion="3.0" schemaVersion="3">
  <file>
    <credentialsProfiles />
    <properties>
      <expanded>True</expanded>
      <name></name>
    </properties>
  </file>
  <connected />
  <favorites />
  <recentlyUsed />
</RDCMan>
'@)

# Set file display name
$xml.RDCMan.file.properties.name = $rootName

# Create the group
$group = $xml.CreateElement("group")
$props = $xml.CreateElement("properties")

$nameEl = $xml.CreateElement("name")
$nameEl.InnerText = $groupName
$expandedEl = $xml.CreateElement("expanded")
$expandedEl.InnerText = "True"

[void]$props.AppendChild($nameEl)
[void]$props.AppendChild($expandedEl)
[void]$group.AppendChild($props)

# Add servers with only the required <properties><name> node
foreach ($s in $servers) {
    $fqdn = $s.Trim()
    if (-not $fqdn) { continue }

    $serverEl   = $xml.CreateElement("server")
    $serverProp = $xml.CreateElement("properties")

    $nameNode   = $xml.CreateElement("name")
    $nameNode.InnerText = $fqdn

    [void]$serverProp.AppendChild($nameNode)
    [void]$serverEl.AppendChild($serverProp)
    [void]$group.AppendChild($serverEl)
}

# Attach group and save
[void]$xml.RDCMan.file.AppendChild($group)
$xml.Save($rdgPath)

Write-Host "RDG created: $rdgPath" -ForegroundColor Green
Invoke-Item $rdgPath  # open with default handler or just File > Open in RDCMan

