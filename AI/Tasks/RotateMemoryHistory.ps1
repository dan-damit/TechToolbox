#!/usr/bin/env pwsh
<#
.SYNOPSIS
    Rotates TechToolbox AI agent memory history to prevent unbounded growth.

.DESCRIPTION
    Implements a rotation policy that:
    - Keeps only the N most recent entries in memory.json (active working
      memory)
    - Archives older entries to memory.history.json (cold storage)
    - Recalculates trend summary statistics based on remaining active entries

.PARAMETER MaxActiveEntries
    Maximum number of history entries to keep in memory.json (default: 20)

.PARAMETER MemoryPath
    Path to the memory.json file (default: AI/memory.json in repo root)

.EXAMPLE
    .\RotateMemoryHistory.ps1

    Rotates history with default settings (keep 20 active entries).

.EXAMPLE
    .\RotateMemoryHistory.ps1 -MaxActiveEntries 50

    Keeps 50 most recent entries in active memory.
#>

param(
    [int]$MaxActiveEntries = 20,
    [string]$MemoryPath = "$PSScriptRoot/../memory.json"
)

# Resolve paths
$MemoryPath = Resolve-Path $MemoryPath -ErrorAction Stop
$ArchivePath = Join-Path (Split-Path $MemoryPath) 'memory.history.json'

Write-Host "🔄 TechToolbox Memory Rotation" -ForegroundColor Cyan
Write-Host "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# Load current memory
$memory = Get-Content $MemoryPath -Raw | ConvertFrom-Json
$currentCount = $memory.history.Count

# Load or initialize archive
if (Test-Path $ArchivePath) {
    $archive = Get-Content $ArchivePath -Raw | ConvertFrom-Json
    if ($archive -isnot [System.Collections.Generic.List[object]] -and $archive -isnot [array]) {
        $archive = @($archive)
    }
}
else {
    $archive = @()
}

Write-Host "Current state:"
Write-Host "  Active entries: $currentCount"
Write-Host "  Archive entries: $($archive.Count)"
Write-Host "  Max active: $MaxActiveEntries"
Write-Host ""

# Rotate if necessary
if ($currentCount -gt $MaxActiveEntries) {
    $toArchive = $currentCount - $MaxActiveEntries

    # Move oldest entries to archive
    $archive += @($memory.history[0..($toArchive - 1)])

    # Keep newest entries
    $memory.history = @($memory.history[$toArchive..($currentCount - 1)])

    Write-Host "✓ Rotated $toArchive entries to archive" -ForegroundColor Green
}
else {
    Write-Host "ℹ No rotation needed" -ForegroundColor Yellow
}

# Recalculate trend summary based on active history
Write-Host ""
Write-Host "Updating trend summary..."

if ($memory.history.Count -gt 0) {
    $successCount = @($memory.history | Where-Object { $_.status -eq 'success' }).Count
    $errorCount = @($memory.history | Where-Object { $_.status -eq 'error' }).Count
    $totalCount = $memory.history.Count
    $successRate = if ($totalCount -gt 0) { $successCount / $totalCount } else { 0 }

    $avgDuration = 0
    $avgTools = 0

    if ($totalCount -gt 0) {
        $durations = @($memory.history.durationMs)
        $avgDuration = [int]($durations | Measure-Object -Average | Select-Object -ExpandProperty Average)

        $toolCalls = @($memory.history.toolCalls)
        $avgTools = [math]::Round(($toolCalls | Measure-Object -Average | Select-Object -ExpandProperty Average), 2)
    }

    $completedCount = @($memory.history | Where-Object { $_.outcome -eq 'completed' }).Count
    $limitCount = @($memory.history | Where-Object { $_.outcome -eq 'iteration-limit' }).Count

    # Update trend summary
    $memory.facts.trendSummary = @{
        windowItems         = $totalCount
        runCount            = $totalCount
        successRate         = $successRate
        avgDurationMs       = $avgDuration
        avgToolCalls        = $avgTools
        statusCounts        = @{
            success = $successCount
            error   = $errorCount
        }
        outcomeCounts       = @{
            completed         = $completedCount
            'iteration-limit' = $limitCount
        }
        lastStatus          = $memory.history[-1].status
        lastOutcome         = $memory.history[-1].outcome
        lastModel           = $memory.history[-1].model
        lastRunTimestampUtc = $memory.history[-1].timestampUtc
        trendLastUpdatedUtc = [System.DateTime]::UtcNow.ToString('o')
    }

    Write-Host "  Success rate: $([math]::Round($successRate * 100))%"
    Write-Host "  Avg duration: ${avgDuration}ms"
    Write-Host "  Avg tool calls: $avgTools"
}

# Save files
$memory | ConvertTo-Json -Depth 10 | Set-Content $MemoryPath
$archive | ConvertTo-Json -Depth 10 | Set-Content $ArchivePath

Write-Host ""
Write-Host "✓ Rotation complete" -ForegroundColor Green
Write-Host "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
Write-Host "Final state:"
Write-Host "  Active entries: $($memory.history.Count) (in memory.json)"
Write-Host "  Archive entries: $($archive.Count) (in memory.history.json)"
Write-Host ""

