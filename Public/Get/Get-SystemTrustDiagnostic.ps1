function Get-SystemTrustDiagnostic {
    <#
    .SYNOPSIS
        Collects health and configuration information relevant to system trust
        on Windows endpoints.

    .PARAMETER ComputerName
        The target computer(s) to query. Defaults to the local computer if not
        specified.

    .PARAMETER Credential
        Credential used for WSMan/SSH remoting (optional).

    .PARAMETER UseSsh
        Use SSH transport instead of WSMan.

    .PARAMETER Port
        SSH port (default 22).

    .PARAMETER Ps7ConfigName
        WSMan endpoint name for PS7 (default 'PowerShell.7').

    .PARAMETER WinPsConfigName
        WSMan endpoint name for Windows PowerShell (default
        'Microsoft.PowerShell').

    .PARAMETER UserName
        SSH username (optional if Credential provided).

    .PARAMETER KeyFilePath
        SSH key path for key-based auth.

    .OUTPUTS
        A custom object with properties for each trust section (Secure Boot,
        TPM, Windows Trust, and general System info). Each section includes a
        health state, condition description, and relevant evidence.
    #>
    [CmdletBinding()]
    param(
        [Parameter(ValueFromPipeline)]
        [string[]]$ComputerName = $env:COMPUTERNAME,

        [pscredential]$Credential,

        [switch]$UseSsh,
        [int]$Port = 22,

        [string]$Ps7ConfigName = 'PowerShell.7',
        [string]$WinPsConfigName = 'Microsoft.PowerShell',

        [string]$UserName,
        [string]$KeyFilePath
    )

    begin {
        Initialize-TechToolboxRuntime

        # Resolve module root + worker locations
        $moduleRoot = Get-ModuleRoot

        # Normalize:
        $workerDir = $script:cfg.workerPath.default
        if (-not [System.IO.Path]::IsPathRooted($workerDir)) {
            $workerDir = Join-Path $moduleRoot $workerDir
        }
        # Worker payload source (ps1 + json) - these files must be "worker-safe"
        # (no reliance on module auto-loading; remote will dot-source these directly)
        $workerHelpersRoot = $script:cfg.trustWorkerHelpersRoot.default
        if (-not [System.IO.Path]::IsPathRooted($workerHelpersRoot)) {
            $workerHelpersRoot = Join-Path $moduleRoot $workerHelpersRoot
        }

        # Remote placement for worker script (you always copy it, so it can be stable)
        $workerRemote = 'C:\ProgramData\TechToolbox\Workers\Get-SystemTrustDiagnostic.Worker.ps1'
        # Local path to worker script (dot-sourced for local execution)
        $workerLocal = Join-Path $workerDir 'Get-SystemTrustDiagnostic.Worker.ps1'

        # Helper payload to zip for remote. Include JSON templates.
        $helperFiles = @(
            Join-Path $workerHelpersRoot 'Get-SecureBootSection.ps1'
            Join-Path $workerHelpersRoot 'Get-TPMSection.ps1'
            Join-Path $workerHelpersRoot 'Get-WindowsTrustSection.ps1'
            Join-Path $workerHelpersRoot 'Get-SystemSection.ps1'
            Join-Path $workerHelpersRoot 'New-TrustSectionResult.ps1'
            Join-Path $workerHelpersRoot 'Resolve-TrustStatusMessage.ps1'
            Join-Path $workerHelpersRoot 'StatusTemplates.Trust.json'
        )

        # Cache package so we don't rebuild per host
        $helpersPkg = $null

        function Test-IsLocalTarget {
            param([string]$Name)

            if ([string]::IsNullOrWhiteSpace($Name)) { return $true }

            $n = $Name.Trim().ToLowerInvariant()
            if ($n -in @('.', 'localhost', '127.0.0.1', '::1')) { return $true }

            # Match computername or FQDN that starts with computername.
            $local = $env:COMPUTERNAME.ToLowerInvariant()
            if ($n -eq $local) { return $true }
            if ($n.StartsWith("$local.")) { return $true }

            return $false
        }
    }

    process {
        foreach ($cn in $ComputerName) {

            if (Test-IsLocalTarget -Name $cn) {
                # --- LOCAL PATH ---
                # Private\ functions should already be loaded by module import.
                # Dot-source the worker so its entry function is available.
                . $workerLocal

                # Worker needs HelpersPath only to find the JSON template file (assets).
                $helpersPath = $workerHelpersRoot

                Get-SystemTrustDiagnosticCore -HelpersPath $helpersPath
                continue
            }

            # --- REMOTE PATH ---

            # Build helpers package once (first remote target)
            if (-not $helpersPkg) {
                $helpersPkg = New-HelpersPackage -HelperFiles $helperFiles
                if (-not $helpersPkg.ZipPath) {
                    throw "Failed to build helpers package (ZipPath is null)."
                }
            }

            # Create session using your PS7-first helper (WSMan/SSH)
            $session = $null
            try {
                $session = Start-NewPSRemoteSession `
                    -ComputerName   $cn `
                    -Credential     $Credential `
                    -UseSsh:$UseSsh `
                    -Port           $Port `
                    -Ps7ConfigName  $Ps7ConfigName `
                    -WinPsConfigName $WinPsConfigName `
                    -UserName       $UserName `
                    -KeyFilePath    $KeyFilePath

                Invoke-RemoteWorker `
                    -Session          $session `
                    -HelpersZip       $helpersPkg.ZipPath `
                    -HelpersZipHash   $helpersPkg.ZipHash `
                    -WorkerRemotePath $workerRemote `
                    -WorkerLocalPath  $workerLocal `
                    -EntryPoint       'Get-SystemTrustDiagnosticCore' `
                    -EntryParameters  @{}   # HelpersPath injected by engine
            }
            finally {
                if ($session) {
                    Remove-PSSession $session -ErrorAction SilentlyContinue
                }
            }
        }
    }

    end {
        # Remove cached helpers ZIP once after all processing
        if ($helpersPkg -and $helpersPkg.ZipPath -and (Test-Path -LiteralPath $helpersPkg.ZipPath)) {
            Remove-Item -LiteralPath $helpersPkg.ZipPath -Force -ErrorAction SilentlyContinue
        }
    }
}

