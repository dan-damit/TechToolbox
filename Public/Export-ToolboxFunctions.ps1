function Export-ToolboxFunctions {
    <#
    .SYNOPSIS
        Discovers and validates public function names from the module Public directory.

    .DESCRIPTION
        Recursively scans $ModuleRoot/Public for *.ps1 files, parses declared function
        names, optionally filters by function name or file basename, validates that those
        names currently resolve to functions, and returns a unique list of names.
        This function does not dot-source files and does not call Export-ModuleMember;
        the caller controls import/export timing.

    .PARAMETER ModuleRoot
        The root directory of the TechToolbox module (defaults to $script:ModuleRoot).

    .PARAMETER SelectiveList
        Optional. If provided, only functions in this array are exported instead of all public files.

    .EXAMPLE
        Export-ToolboxFunctions

    .EXAMPLE
        Export-ToolboxFunctions -SelectiveList @('Get-TechToolboxEnvironmentHealth', 'Invoke-PurviewPurge')
    #>
    [CmdletBinding()]
    [OutputType([string[]])]
    param(
        [string]$ModuleRoot = $script:ModuleRoot,

        [string[]]$SelectiveList
    )

    $trace = Get-Command -Name '__tt_trace' -ErrorAction SilentlyContinue
    if ($trace) { __tt_trace "Starting Export-ToolboxFunctions" }

    $publicRoot = Join-Path $ModuleRoot 'Public'
    if (-not (Test-Path -Path $publicRoot -PathType Container)) {
        if ($trace) { __tt_trace "WARNING: Public directory not found at '$publicRoot'." }
        return @()
    }

    $allFiles = Get-ChildItem -Path $publicRoot -Recurse -Filter *.ps1 -File
    if ($null -eq $allFiles -or $allFiles.Count -eq 0) {
        if ($trace) { __tt_trace "No public .ps1 files found. Skipping discovery." }
        return @()
    }

    $entries = foreach ($file in $allFiles) {
        $content = Get-Content -Path $file.FullName -Raw
        $match = [regex]::Match($content, '(?im)^\s*function\s+([A-Za-z0-9_-]+)\b')
        if ($match.Success) {
            [pscustomobject]@{
                Name     = $match.Groups[1].Value
                BaseName = $file.BaseName
                FileName = $file.Name
            }
        }
    }

    if ($SelectiveList -and $SelectiveList.Count -gt 0) {
        if ($trace) { __tt_trace "Selective export requested: $($SelectiveList -join ', ')" }
        $entries = $entries | Where-Object { $_.Name -in $SelectiveList -or $_.BaseName -in $SelectiveList }
    }

    if ($null -eq $entries) {
        if ($trace) { __tt_trace "No matching function declarations found in Public scripts." }
        return @()
    }

    $candidateNames = $entries | Select-Object -ExpandProperty Name -Unique
    $resolvedNames = [System.Collections.Generic.List[string]]::new()

    foreach ($name in $candidateNames) {
        if (Test-Path -Path ("function:{0}" -f $name)) {
            [void]$resolvedNames.Add($name)
        }
        elseif ($trace) {
            __tt_trace "Skipping unresolved function declaration: $name"
        }
    }

    if ($trace) {
        __tt_trace ("Export-ToolboxFunctions complete. Resolved: {0}" -f $resolvedNames.Count)
    }

    return $resolvedNames.ToArray()
}
