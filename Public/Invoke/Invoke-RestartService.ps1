function Invoke-RestartService {
    <#
    .SYNOPSIS
        Restarts a named Windows service locally or on one or more remote
        computers.

    .DESCRIPTION
        Invoke-RestartService performs a controlled restart of a specific
        service using the shared Restart-ServiceWorker logic.

        Workflow summary:
        - Initializes TechToolbox runtime.
        - Iterates through each target in ComputerName.
        - Uses a local path for the current host/localhost.
        - Uses a remote PowerShell session path for non-local targets.
        - Calls Restart-ServiceWorker with ServiceName, TimeoutSeconds, and
          Force.
        - Cleans up remote sessions in finally blocks.
        - Logs per-target progress and errors via Write-Log.

        The function supports ShouldProcess (-WhatIf/-Confirm). When WhatIf or
        Confirm blocks an action, restart is skipped for that target.

    .PARAMETER ServiceName
        Name of the Windows service to restart. This is passed directly to
        Restart-ServiceWorker.

    .PARAMETER ComputerName
        One or more computer names to target. Defaults to the current computer.
        Accepts values from pipeline input.

        Local targets:
        - Current computer name ($env:COMPUTERNAME)
        - localhost

        Non-local targets are handled through Start-NewPSRemoteSession and
        Invoke-Command.

    .PARAMETER TimeoutSeconds
        Maximum number of seconds to wait for restart operations in the worker.
        Default is 30.

    .PARAMETER Force
        Requests a forced restart behavior in Restart-ServiceWorker.

    .INPUTS
        System.String. You can pipe computer names to this function.

    .OUTPUTS
        None. Operational status is emitted through logging.

    .NOTES
        - Requires rights to control services on target systems.
        - Remote execution requires WinRM/PowerShell remoting connectivity and
          authorization.
        - Errors are logged per target; function continues processing remaining
          computers.

    .EXAMPLE
        PS> Invoke-RestartService -ServiceName "Spooler"
        Restarts the Print Spooler service on the local computer.

    .EXAMPLE
        PS> Invoke-RestartService -ServiceName "wuauserv" -ComputerName "SRV01","SRV02" -TimeoutSeconds 60
        Restarts Windows Update service on two remote servers, allowing up to 60
        seconds for worker operations.

    .EXAMPLE
        PS> "SRV01","SRV02" | Invoke-RestartService -ServiceName "LanmanServer" -Force
        Pipes computer names and performs a forced restart request for the Server
        service on each target.

    .EXAMPLE
        PS> Invoke-RestartService -ServiceName "BITS" -ComputerName "SRV01" -WhatIf
        Shows what would be restarted without performing the action.
    #>
    [CmdletBinding(SupportsShouldProcess, ConfirmImpact = 'Medium')]
    param(
        [Parameter(Mandatory)]
        [string]$ServiceName,

        [Parameter(ValueFromPipeline)]
        [string[]]$ComputerName = $env:COMPUTERNAME,

        [int]$TimeoutSeconds = 30,

        [switch]$Force
    )

    Initialize-TechToolboxRuntime

    foreach ($computer in $ComputerName) {

        Write-Log -Level Info -Message "[$computer] Processing service '$ServiceName'..."

        # Local execution path
        if ($computer -eq $env:COMPUTERNAME -or $computer -eq 'localhost') {

            if ($PSCmdlet.ShouldProcess("$computer : $ServiceName", "Restart service")) {
                Restart-ServiceWorker `
                    -ServiceName $ServiceName `
                    -TimeoutSeconds $TimeoutSeconds `
                    -Force:$Force
            }

            continue
        }

        # Remote execution path
        try {
            $session = Start-NewPSRemoteSession -ComputerName $computer

            if ($PSCmdlet.ShouldProcess("$computer : $ServiceName", "Restart service")) {
                Invoke-Command `
                    -Session $session `
                    -ScriptBlock ${function:Restart-ServiceWorker} `
                    -ArgumentList $ServiceName, $TimeoutSeconds, $Force
            }
        }
        catch {
            Write-Log -Level Error -Message "[$computer] Failed: $($_.Exception.Message)"
        }
        finally {
            if ($session) {
                Remove-PSSession -Session $session
            }
        }
    }
}

